def get_downcast_part(binned_data_DC, binned_data_UC):

    # Identify where the DC and UC are within the min error of each other
    min_error = 0.06


    depths_within_error = binned_data_DC["Depth Bin"][np.abs((binned_data_DC["Absolute Salinity"]-binned_data_UC["Absolute Salinity"])) < min_error].tolist()

    buffer_length=10
    cutoff_depth=0

    for i in range(len(depths_within_error)):
        if depths_within_error[i]+buffer_length==depths_within_error[i+buffer_length]: #10 consecutive meters (note depths are negative)
            cutoff_depth=depths_within_error[i+buffer_length]
            break

    trimmed_binned_DC = binned_data_DC[binned_data_DC["Depth Bin"] > cutoff_depth] #trims data by depth bin

    return trimmed_binned_DC, cutoff_depth




def add_upcast_patch(binned_data_DC, binned_data_UC):

    #Get trimmed binned downcast part
    trimmed_binned_DC, cutoff_depth  = get_downcast_part(binned_data_DC, binned_data_UC)

    #Get trimmed binned upcast patch
    UC_patch = binned_data_UC[binned_data_UC["Depth Bin"] <= cutoff_depth]

    #Stick them together
    uc_patched_data = pd.concat([trimmed_binned_DC, UC_patch], ignore_index=True)
    uc_patched_data = uc_patched_data.sort_values("Depth Bin", ascending = True).reset_index(drop=True) #reorder by depth, with shallowest first

    return uc_patched_data





