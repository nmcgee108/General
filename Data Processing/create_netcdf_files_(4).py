def add_other_vars(dataframe, binned_data_DC, stats_dict):
    # Only keep depth, turbidity, fluorescence from downcast
    turb_fluor_df = binned_data_DC[["Depth Bin", "Turbidity", "Fluorescence"]].copy()

    # Merge both dataframes on Depth Bin
    merged_df = pd.merge(dataframe, turb_fluor_df, on="Depth Bin", how="outer", suffixes=('', '_new'))

    # Replace turbidity and fluorescence if available
    for var in ["Turbidity", "Fluorescence"]:
        merged_df[var] = merged_df[f"{var}_new"].combine_first(merged_df[var])
        merged_df = merged_df.drop(columns=[f"{var}_new"])

    # Recalculate SA and CT
    SA = pd.Series(index=merged_df.index, dtype=float)
    CT = pd.Series(index=merged_df.index, dtype=float)

    valid = merged_df[["Practical Salinity", "Pressure", "Potential Temp"]].notnull().all(axis=1)
    SA[valid] = gsw.SA_from_SP(merged_df.loc[valid, "Practical Salinity"],
                               merged_df.loc[valid, "Pressure"],
                               stats_dict["Longitude"],
                               stats_dict["Latitude"])
    CT[valid] = gsw.CT_from_pt(SA[valid], merged_df.loc[valid, "Potential Temp"])

    merged_df["Absolute Salinity"] = SA
    merged_df["Conservative Temp"] = CT

    return merged_df



