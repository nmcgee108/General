def remove_spikes(dataframe):
    
    # Replace known "fake NaNs" (e.g., -9.990e-29) with real NaNs
    dataframe = dataframe.replace(-9.990e-29, np.nan)

    #columns to check for spikes
    columns_to_check = [
        "Conductivity",
        "Conservative Temp",
        "Potential Temp",
        "Oxygen",
        "Absolute Salinity",
        "Practical Salinity"]

    #parameters for spike detection
    window_size = 50 #window for defining rolling mean
    threshold = 3  #number of st devs away from mean

    #boolean mask to keep rows (start with all True)
    valid_rows = pd.Series(True, index=dataframe.index)

    for col in columns_to_check:
        series = dataframe[col]
        rolling_mean = series.rolling(window=window_size, center=True).mean()
        rolling_std = series.rolling(window=window_size, center=True).std()

        # identify where the absolute deviation from the mean exceeds threshold × std
        spike_mask = (np.abs(series - rolling_mean) > threshold * rolling_std)

        spike_mask.fillna(False) #make NaNs false (no spike)

        #valid rows are only when spike mask is false
        valid_rows &= ~spike_mask

    # Apply the mask to remove spike rows
    dataframe_cleaned = dataframe[valid_rows].reset_index(drop=True)

    return dataframe_cleaned


