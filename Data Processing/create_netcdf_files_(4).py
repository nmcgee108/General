"""# Write to NetCDF Function"""

import os
from netCDF4 import Dataset
from datetime import datetime

def write_to_netCDF(df, stats_dict, filename, flag_type):
    """
    Appends a CTD cast (df + stats_dict) to a NetCDF file using VLEN arrays
    to handle varying depths per cast.

    Parameters:
        df (pd.DataFrame): Profile data (depth, temperatures, salinities, etc.)
        stats_dict (dict): Metadata with keys like 'latitude', 'longitude', 'date', 'cast_number'
        filename (str): Output NetCDF filename
    """
    # Ensure consistent column order and float dtype
    df = df.astype('float32').reset_index(drop=True)

    variables = ['Depth Bin',
                 "Absolute Salinity",
                 "Practical Salinity",
                 "Potential Temp",
                 "Conservative Temp",
                 "Pressure",
                 "Conductivity",
                 "Oxygen",
                 "Turbidity",
                 "Fluorescence"]

    # create the NetCDF file if it doesnt exist yet
    if not os.path.exists(filename):
        ncfile = Dataset(filename, 'w', format='NETCDF4')
        ncfile.createDimension('cast', None)  # Unlimited dimension

        # Create metadata variables
        ncfile.createVariable('Latitude', 'f4', ('cast',))
        ncfile.createVariable('Longitude', 'f4', ('cast',))
        ncfile.createVariable('Cast_Number', 'i4', ('cast',))
        ncfile.createVariable('Date', 'i8', ('cast',))  #saves date as timestamp
        ncfile.createVariable('Flag', 'i8', ('cast',))

        # Create VLEN type
        vlen_float = ncfile.createVLType(np.float32, 'vlen_float')

        # Create profile variables
        for var in variables:
            ncfile.createVariable(var, vlen_float, ('cast',))

        cast_index = 0

    # if the file already exists
    else:
        ncfile = Dataset(filename, 'a') #open file in append mode
        cast_index = len(ncfile.dimensions['cast']) #how many casts are already in it

    # write metadata
    ncfile['Latitude'][cast_index] = stats_dict['Latitude']
    ncfile['Longitude'][cast_index] = stats_dict['Longitude']
    ncfile['Cast_Number'][cast_index] = stats_dict['Cast Number']
    date = pd.to_datetime(stats_dict['Date'])
    ncfile['Date'][cast_index] = int(date.timestamp()) #put the date in the right format
    ncfile['Flag'][cast_index] = flag_type


    # write each profile variable (VLEN arrays)
    for var in variables:
        ncfile[var][cast_index] = df[var].to_numpy(dtype='float32')

    ncfile.close()

